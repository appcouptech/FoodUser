package food.user.demand.FCPojo.FCHotelDetailsActivityObject;

public class MenuListObject {

    private String d_images ;
    private String d_info ;
    private String category_id ;
    private String category_name ;
    private String count ;
    private String scrollPostion ;

    public String getD_images() {
        return d_images;
    }

    public void setD_images(String d_images) {
        this.d_images = d_images;
    }

    public String getD_info() {
        return d_info;
    }

    public void setD_info(String d_info) {
        this.d_info = d_info;
    }


    public String getCategory_id() {
        return category_id;
    }

    public void setCategory_id(String category_id) {
        this.category_id = category_id;
    }

    public String getCategory_name() {
        return category_name;
    }

    public void setCategory_name(String category_name) {
        this.category_name = category_name;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }


    public String getScrollPostion() {
        return scrollPostion;
    }

    public void setScrollPostion(String scrollPostion) {
        this.scrollPostion = scrollPostion;
    }
}
