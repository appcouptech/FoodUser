package food.user.demand.FCPojo.FCCartActivityObject;

public class PricingObject {

    private String d_images ;
    private String d_info ;
    private String id ;
    private String product_id ;
    private String foodsize_id ;
    private String foodsize_name ;
    private String price ;
    private String currency ;

    public String getD_images() {
        return d_images;
    }

    public void setD_images(String d_images) {
        this.d_images = d_images;
    }

    public String getD_info() {
        return d_info;
    }

    public void setD_info(String d_info) {
        this.d_info = d_info;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getProduct_id() {
        return product_id;
    }

    public void setProduct_id(String product_id) {
        this.product_id = product_id;
    }

    public String getFoodsize_id() {
        return foodsize_id;
    }

    public void setFoodsize_id(String foodsize_id) {
        this.foodsize_id = foodsize_id;
    }

    public String getFoodsize_name() {
        return foodsize_name;
    }

    public void setFoodsize_name(String foodsize_name) {
        this.foodsize_name = foodsize_name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }
}
