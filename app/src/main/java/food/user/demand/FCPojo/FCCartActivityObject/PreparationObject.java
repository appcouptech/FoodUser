package food.user.demand.FCPojo.FCCartActivityObject;

public class PreparationObject {

    private String d_images ;
    private String d_info ;
    private String id ;
    private String preparation_type ;
    private String status ;


    public String getD_images() {
        return d_images;
    }

    public void setD_images(String d_images) {
        this.d_images = d_images;
    }

    public String getD_info() {
        return d_info;
    }

    public void setD_info(String d_info) {
        this.d_info = d_info;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getPreparation_type() {
        return preparation_type;
    }

    public void setPreparation_type(String preparation_type) {
        this.preparation_type = preparation_type;
    }
}
